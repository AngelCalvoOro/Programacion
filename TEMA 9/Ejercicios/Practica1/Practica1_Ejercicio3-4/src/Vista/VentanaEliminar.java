    private void jbaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbaceptarActionPerformed
        try {
            String nombreDelete = jtfnombre.getText();
            if (nombreDelete.isEmpty()) {
                throw new EventoVacionException(1);
            }
            Control.deleteEvento(nombreDelete);
            JOptionPane.showMessageDialog(this, "Se ha eliminado el evento correctamente.");
            recargarLista();
        } catch (EventoVacionException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_jbaceptarActionPerformed

