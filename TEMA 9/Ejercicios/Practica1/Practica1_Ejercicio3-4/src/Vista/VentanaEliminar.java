    public void mostrarEventos(ArrayList<Evento> listaEventos){
        String nombres="";
        for (int i = 0; i < listaEventos.size(); i++) {
            Evento e=listaEventos.get(i);
            nombres += e.getNombre()+"\n";
        }

        this.jtalistaevento.setText(nombres);
    }
